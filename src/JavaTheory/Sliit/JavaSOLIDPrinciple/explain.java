package JavaTheory.Sliit.JavaSOLIDPrinciple;



// Single Responsibility Principle (SRP):
//A class should have only one reason to change, meaning that a class should have only one responsibility or job.
//This principle encourages a separation of concerns and helps in creating smaller, more focused classes, making the code easier to understand, maintain, and extend.

//Open/Closed Principle (OCP):
//Software entities (classes, modules, functions, etc.) should be open for extension but closed for modification.
//This principle encourages the use of abstraction and interfaces to allow for adding new functionality without altering existing code. This promotes a more stable and extensible system.

//Liskov Substitution Principle (LSP):
//Objects of a superclass should be able to be replaced with objects of a subclass without affecting the correctness of the program.
//In simpler terms, if a class is a subclass of another class, it should be able to replace its parent class without causing any issues. This ensures that the behavior of the base class is preserved in its derived classes.

//Interface Segregation Principle (ISP):
//A class should not be forced to implement interfaces it does not use.
//Instead of having large, monolithic interfaces, this principle suggests breaking them down into smaller, more specific interfaces. This way, a class can implement only the interfaces relevant to its functionality, avoiding unnecessary dependencies.

//Dependency Inversion Principle (DIP):
//High-level modules should not depend on low-level modules. Both should depend on abstractions.
//Abstractions should not depend on details; details should depend on abstractions.
//This principle promotes the use of dependency injection and inversion of control to achieve a more flexible and decoupled architecture, making it easier to replace components and manage dependencies.

